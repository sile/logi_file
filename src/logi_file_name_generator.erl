%% @doc Log Filename Generator Interface
%% @private
-module(logi_file_name_generator).

%%------------------------------------------------------------------------------------------------------------------------
%% Exported API
%%------------------------------------------------------------------------------------------------------------------------
-export([generate/1]).

-export_type([state/0]).

%%------------------------------------------------------------------------------------------------------------------------
%% Types
%%------------------------------------------------------------------------------------------------------------------------
-type state() :: tuple().

%%------------------------------------------------------------------------------------------------------------------------
%% Behaviour Callbacks
%%------------------------------------------------------------------------------------------------------------------------
-callback generate(state()) -> {FileName::binary(), state()}.

%%------------------------------------------------------------------------------------------------------------------------
%% Exported Functions
%%------------------------------------------------------------------------------------------------------------------------
%% @doc ログファイル名を生成する
-spec generate(state()) -> {FileName::binary(), state()}.
generate(State) ->
    (element(1, State)):generate(State).
